/****************************************************************************
 * Name: AT24C02_init
 *
 * Description:
 *    初始化IIC的IO口
 *
 * Parameters:
 *
 * Returned Value:
 *
 * Assumptions:
 *
 ****************************************************************************/
void at24c02_init(void);

/****************************************************************************
 * Name: at24c02_read
 *
 * Description:
 *    从指定PAGE读取指定长度数据
 *
 * Parameters:
 *    page 从那一页码读
 *    buf 数据缓存
 *    len 读取的长度不超过16个byte
 *
 * Returned Value:
 *    true 读取成功，false读取失败
 *
 * Assumptions:
 *
 ****************************************************************************/
	int at24c02_read(unsigned char addr, unsigned char* buf, int len);
/****************************************************************************
 * Name: at24c02_write
 *
 * Description:
 *    向EEPROM的指页写入指定长度数据
 *
 * Parameters:
 *    page 页码
 *    buf 数据缓存
 *    len 写入的长度
 *
 * Returned Value:
 *    true 写入成功，false 写入失败
 *
 * Assumptions:
 *
 ****************************************************************************/
bool at24c02_write(unsigned char page, unsigned char* buf, int len);

unsigned char  at24c02_read_one_byte(unsigned char addr);
bool at24c02_write_one_byte(unsigned char addr, unsigned char data);


